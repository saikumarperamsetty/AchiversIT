# Ex:28 max(), min() and sum() methods on list in Python?
# max()
l = [10,90,40,50,30]
print(max(l))           # output = 90

# min()
l = [10,90,40,50,30]
print(min(l))           # output = 10

# sum()
l = [10,20,60,50,40]
print(sum(l))           # output = 180